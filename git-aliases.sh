# To source and manage all git related aliases in one place

alias g="git"
alias gst="git status"
alias gco="git checkout"
alias ga="git add"
alias gc="git commit"
alias gam="git commit --amend"
alias gameo="git commit --amend --no-edit"
alias gd="git diff"
alias gds="git diff --staged"
alias gp="git push -v"
alias gup="git pull -v --rebase --autostash"
